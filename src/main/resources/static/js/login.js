$(function(){
	ajaxForm({
		form:$('#login-form'),
		url:'login',
		success:function(){
			$(location).attr('href',$("meta[name='_login_return_url']").attr("content"));
		},
		error:function(err){
			show_alert('danger',err.errorMessage);
		},
		before:function(){
			close_alert();
			$('#login-btn').prop('disabled',true);
			$('#login-btn').text('Login...');
		},
		after:function(){
			$('#login-btn').prop('disabled',false);
			$('#login-btn').text('Login');
		}
	});
});
